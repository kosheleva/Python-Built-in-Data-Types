a = int(10)
b = 20

print('Numbers:', a, b)

print('Add: ', a + b)
print('Subtract: ', b - a)
print('Multiply: ', a * b)
print('Divide: ', b / a)
print('Mod: ', b % a)
print('Pov: ', pow(b, a), b ** a)

print('>: ', a > b)
print('<: ', a < b)
print('>=: ', a >= b)
print('<=: ', a <= b)
print('==: ', a == b)

